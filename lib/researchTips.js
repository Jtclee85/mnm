// 자료분석 로딩 중에 보여주는 자료조사 꿀팁.
// 학생이 기다리는 시간을 학습 기회로 바꾸는 것이 목적이므로,
// 이해·탐구·표현·성찰 활동과 이어지는 조사 습관 중심으로 구성한다.
export const RESEARCH_TIPS = [
  '자료를 읽을 때는 먼저 제목과 첫 문장을 살펴보면 핵심을 잡기 쉬워요.',
  '처음 보는 낱말은 그냥 넘기지 말고 뜻을 확인해 보세요.',
  '자료에서 반복해서 나오는 말은 중요한 핵심 낱말일 가능성이 높아요.',
  '누가, 언제, 어디에서 만든 자료인지 확인하면 더 믿을 수 있어요.',
  '내 생각을 쓸 때는 자료에서 찾은 근거를 함께 적어 보세요.',
  '한 자료만 보지 말고, 다른 자료와 비교하면 더 깊이 이해할 수 있어요.',
  '사진, 지도, 연표는 글로 된 자료를 이해하는 데 큰 도움이 돼요.',
  '사실과 내 생각을 구분해서 정리하면 글쓰기와 발표가 쉬워져요.',
  '궁금한 점을 질문으로 바꾸면 좋은 탐구 주제가 될 수 있어요.',
  '자료를 그대로 베끼기보다 내 말로 바꾸어 정리하는 것이 중요해요.',
];

// 현재 팁과 겹치지 않는 다음 팁 인덱스를 랜덤으로 고른다.
export function pickNextTipIndex(currentIndex = -1) {
  if (RESEARCH_TIPS.length <= 1) return 0;
  let next = currentIndex;
  while (next === currentIndex) {
    next = Math.floor(Math.random() * RESEARCH_TIPS.length);
  }
  return next;
}
